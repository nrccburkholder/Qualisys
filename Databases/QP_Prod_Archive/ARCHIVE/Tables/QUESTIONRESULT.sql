CREATE TABLE [ARCHIVE].[QUESTIONRESULT] (
    [QUESTIONRESULT_ID] INT NOT NULL,
    [QUESTIONFORM_ID]   INT NOT NULL,
    [SAMPLEUNIT_ID]     INT NOT NULL,
    [QSTNCORE]          INT NOT NULL,
    [INTRESPONSEVAL]    INT NOT NULL,
    [QPC_TIMESTAMP]     INT NULL,
    [ArchiveRunID]      INT NULL,
